<H3>What does FASHION STATEMENT do?</H3>

FASHION STATEMENT allows users to browse and 'like' clothing items within the web site database.

The team hopes that FASHION STATEMENT will allow all users to broaden their perspective on modern fashion and share their tastes with other users!
 

